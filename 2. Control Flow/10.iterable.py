print(type(5))
print(type(range(5)))

# range function returns a object type range
# range is a example of complex types
# range object is iterable

for x in range(5):
    print(".")
# so the range function returns an object which is iterable

for x in "Python":
    print(x)
# string objects are also iterable

for x in [1, 2, 3, 4]:
    print(x)
# list is also iterable
